//Print Odd Numbers
for(let i=1;i<=15;i++){
    if(i%2!=0){
        console.log(i);
    }
}

console.log("\n");
//Print Even Numbers
for(let i=1;i<=15;i++){
    if(i%2==0){
        console.log(i);
    }
}

